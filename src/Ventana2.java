    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        numero1=Float.parseFloat(txtparcial1.getText());
        numero2=Float.parseFloat(txtparcial2.getText());
        BECA=(String) Opc_Beca.getSelectedItem();
        
        promedio=(numero1+numero2)/2;
        
        String []info=new String [8];
        info[0]=txtnombre.getText();
        info[1]=txtjornada.getText();
        info[2]=txtnivel.getText();
        info[3]=txtparcial1.getText();
        info[4]=txtparcial2.getText();
        info[5]=String.valueOf(promedio);
        if(promedio<4){
        info[6]="REPROVADO";
        }
        else if(promedio>=4 & promedio<7){
        info[6]="SUSPENDIDO";
        }
        else if(promedio>=7){
        info[6]="APROVADO";
        }else{
        info[6]="MAL INGRESADO";
        }
        switch(BECA){
            case ("Si"):
                if (promedio>=7){
                    info[7]="Conserva Beca";
                }else{
                    info[7]="No conserva Beca";
                }
                break;
            case "No":
                info[7]="No tiene Beca";
                break;
        }
        modelo.addRow(info);
        txtnombre.setText("");
        txtjornada.setText("");
        txtnivel.setText("");
        txtparcial1.setText("");
        txtparcial2.setText("");
    }//GEN-LAST:event_jButton3ActionPerformed

